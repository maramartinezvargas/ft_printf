#include "ft_printf.h"

int	ft_printf(char const *format, ...)
{
	va_list	args;
	size_t	i;
	int		count;

	i = 0;
	count = 0;
	va_start(args, format);
	while (format[i])
	{
		if (format[i] == '%')
		{
			i++;
			count+=ft_format(format[i], args);
		}
		else
		{
			count+=ft_putchar(format[i]);
		}			
		i++;
	}
	return (count);
}
